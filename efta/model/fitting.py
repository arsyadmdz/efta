def _constraints_to_acceptable(func) -> dict:
    """
    Convert stored constraints on *func* to ``acceptable_*`` kwargs for
    ``_model_chem``.  Unconstrained params default to ``(0, inf)``.
    """
    constraints = getattr(func, 'constraints', {})
    names       = _param_names(func)
    result      = {}
    for name in names:
        spec = constraints.get(name, (0, float('inf')))
        result[f'acceptable_{name}'] = spec
    return result


def _infer_method(func) -> str:
    """Infer 'cont' or 'ranged' from stored constraints on *func*."""
    constraints = getattr(func, 'constraints', {})
    for spec in constraints.values():
        if isinstance(spec, list):
            return 'ranged'
        if isinstance(spec, tuple) and spec != (0, float('inf')):
            return 'ranged'
    return 'cont' 

