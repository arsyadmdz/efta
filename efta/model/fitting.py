# ---------------------------------------------------------------------------
# Parametric key resolution
# ---------------------------------------------------------------------------
# Data dicts (equilibrium / initial) may contain keys with $(xN) or $(expr)
# placeholders — the same notation used in freaction templates.  These are
# resolved against the current parameter values before each residual
# evaluation so that the species name in the data dict matches the species
# name generated by func(*params).
#
# Examples:
#   'LaCl$(3-x2)[$(x2)+]'  with x2=1  →  'LaCl2[+]'
#   'ML3(HL)$(2*x1-3)(org)' with x1=3 →  'ML3(HL)3(org)'
#
# After substitution the resolved key is normalised through efta's species()
# function so that e.g. '[1+]' is canonicalised to '[+]'.


import re as _re

_KEY_SAFE_NS = {'__builtins__': {}, 'abs': abs, 'round': round,
                'int': int, 'float': float}


def _is_parametric_key(key: str) -> bool:
    """Return True if *key* contains any $(expr) placeholder."""
    return '$(' in key


def _resolve_one_key(key: str, mapping: Dict[int, float]) -> str:
    """Evaluate all $(expr) in *key* using *mapping* {index: value}."""
    from ..species import _find_dollar, _eval_dollar, species as _normalise
    from ..model.freaction import _clean_species
    import re as _re

    result = []
    pos = 0
    while True:
        hit = _find_dollar(key, pos)
        if hit is None:
            result.append(key[pos:])
            break
        result.append(key[pos:hit[0]])
        expr   = hit[2]
        filled = _re.sub(r'x(\d+)',
                         lambda m: str(mapping[int(m.group(1))]),
                         expr)
        try:
            val = float(eval(filled, _KEY_SAFE_NS, {}))
        except Exception as e:
            raise ValueError(
                f"Could not evaluate parametric key expression $({expr!r}): {e}")
        # integer → plain, float → $(n)
        if val == int(val):
            result.append(str(int(val)))
        else:
            result.append(f'$({val:.6g})')
        pos = hit[1]

    raw = ''.join(result)
    # apply same simplification as _build_reaction: drop count-1, drop count-0
    cleaned = _clean_species(raw)
    return _normalise(cleaned)


def _resolve_parametric_keys(data_dict: dict, params) -> dict:
    """
    Return a copy of *data_dict* with any parametric species-name keys
    resolved against the current *params* (positional sequence, 1-indexed
    as x1, x2, …).

    Non-parametric keys are passed through unchanged.  If no key is
    parametric the original dict is returned as-is (zero allocation).

    Results are cached by (param_tuple, keys_tuple) so repeated calls
    with the same params and same dict structure are free.
    """
    if not any(_is_parametric_key(k) for k in data_dict):
        return data_dict                           # fast path: no parametric keys

    # cache key: rounded params + ordered keys of this dict
    cache_key = (tuple(round(float(v), 10) for v in params),
                 tuple(data_dict.keys()))
    cached = _resolve_cache.get(cache_key)
    if cached is not None:
        # rebuild with original values (same keys, same order)
        return {new_k: data_dict[old_k]
                for old_k, new_k in zip(data_dict.keys(), cached)}

    mapping = {i + 1: float(v) for i, v in enumerate(params)}
    resolved_keys = tuple(
        _resolve_one_key(k, mapping) if _is_parametric_key(k) else k
        for k in data_dict.keys()
    )
    _resolve_cache[cache_key] = resolved_keys
    return dict(zip(resolved_keys, data_dict.values()))

# module-level cache for resolved parametric keys
_resolve_cache: dict = {}


# ---------------------------------------------------------------------------
# Result object
# ---------------------------------------------------------------------------

class Model:
    """
    Result of a fitting run.

    Attributes
    ----------
    popt : tuple
        Optimal parameter values in the same order as *func*'s arguments.
    residuals : np.ndarray
        Final residual vector (computed - observed) / observed.
    mse : float
        Mean squared error of normalised residuals.
    rms : float
        Root-mean-square of normalised residuals (= sqrt(mse)).
    success : bool
        Whether the optimiser converged.
    message : str
        Optimiser status message.
    n_iter : int
        Number of function evaluations.
    param_names : list of str
        Parameter names from *func*'s signature.
    """

    def __init__(self, popt, residuals, success, message, n_iter, param_names):
        self.popt        = tuple(popt)
        self.residuals   = np.asarray(residuals)
        self.mse         = float(np.mean(self.residuals ** 2))
        self.rms         = float(np.sqrt(self.mse))
        self.success     = bool(success)
        self.message     = str(message)
        self.n_iter      = int(n_iter)
        self.param_names = list(param_names)

    def __repr__(self) -> str:
        params = ', '.join(f'{n}={v:.4g}'
                           for n, v in zip(self.param_names, self.popt))
        return (f"Model({params}, mse={self.mse:.3e}, rms={self.rms:.3e}, "
                f"success={self.success}, n_iter={self.n_iter})")

    def __str__(self) -> str:
        lines = ['Model']
        lines.append(f"  success  : {self.success}  ({self.message})")
        lines.append(f"  mse      : {self.mse:.4e}")
        lines.append(f"  rms      : {self.rms:.4e}")
        lines.append(f"  n_iter   : {self.n_iter}")
        lines.append("  parameters:")
        for n, v in zip(self.param_names, self.popt):
            lines.append(f"    {n:<12s} = {v:.6g}")
        return '\n'.join(lines)


