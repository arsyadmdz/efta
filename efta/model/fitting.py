def _param_names(func) -> List[str]:
    """Return the parameter names of *func* (excluding 'self').
    
    Handles freaction/freactions objects via their .param_names property,
    and regular callables via inspect.signature.
    """
    if hasattr(func, 'param_names'):
        return list(func.param_names)
    sig = inspect.signature(func)
    return [p for p in sig.parameters if p != 'self']


def _check_fast_path(func, initials, equilibria, n_params):
    """
    Pre-check which data points can use the fast mass-action path.
    Returns a list of bools, one per data point.
    """
    flags = []
    try:
        rxn_sys_test = func(*([1.0] * n_params))
        for i in range(len(initials)):
            flags.append(
                is_data_sufficient(rxn_sys_test, equilibria[i], initials[i])
            )
    except Exception:
        flags = [False] * len(initials)
    return flags


def _build_objective(func, initials, equilibria, maintain, eq_species, v_oa=1.0):
    """
    Return a residual function ``residuals(params) -> np.ndarray``.

    Primary path (fast): direct mass-action law residuals
        r_j = log(K_j) - Σᵢ νᵢⱼ · log(aᵢ)
    evaluated directly from observed equilibrium concentrations.
    No equilibrium solver call needed — analytic and orders of magnitude faster.

    Fallback path (slow): black-box equilibrium solver, used when data is
    insufficient to evaluate mass-action directly.
    """
    n_points  = len(initials)
    n_params  = len(eq_species)   # rough proxy; refined below
    fast_path = None   # lazily initialised on first call

    # Detect parametric stoichiometry: $(xN) or $(expr) in species-name keys.
    # When present, species names change with params so the fast-path cache
    # must be rebuilt every call (stale flags cause wrong residuals or crashes).
    _parametric_stoich = any(
        '$(' in k
        for d in list(equilibria) + list(initials)
        for k in d
    )

    def _residuals(params):
        nonlocal fast_path
        res = []

        # Build reaction once per param set — cached in freaction._rxn_cache
        try:
            rxn_sys_sample = func(*params)
        except Exception:
            return np.array([1e6] * max(len(eq_species), 1) * n_points, dtype=float)

        # Cache fast_path by reaction stoich fingerprint so we don't call
        # is_data_sufficient 17× per evaluation when stoichiometry is parametric.
        stoich_key = tuple(sorted(
            rxn_sys_sample._stoich.items() if hasattr(rxn_sys_sample, '_stoich')
            else ((r._stoich.items() if hasattr(r, '_stoich') else ()) for r in getattr(rxn_sys_sample, '_reactions', []))
        ))
        if fast_path is None or (_parametric_stoich and stoich_key != _residuals._last_stoich_key):
            sample_ceq = _resolve_parametric_keys(equilibria[0], params)
            sample_c0  = _resolve_parametric_keys(initials[0],   params)
            fast_path = [
                is_data_sufficient(rxn_sys_sample,
                                   _resolve_parametric_keys(equilibria[j], params),
                                   _resolve_parametric_keys(initials[j],   params))
                for j in range(n_points)
            ]
            _residuals._last_stoich_key = stoich_key

        for i in range(n_points):
            try:
                rxn_sys = func(*params)
            except Exception:
                res.extend([1e6] * max(len(eq_species), 1))
                continue

            ceq_i = _resolve_parametric_keys(equilibria[i], params)
            c0_i  = _resolve_parametric_keys(initials[i],   params)

            if fast_path[i]:
                # ── fast path: direct mass-action residuals ───────────────
                try:
                    r = mass_action_residuals(rxn_sys, ceq_i, c0_i, v_oa=v_oa)
                    res.extend(r.tolist())
                except Exception:
                    res.extend([1e6] * max(len(eq_species), 1))
            else:
                # ── fallback: black-box equilibrium solver ────────────────
                c0 = c0_i if c0_i else ceq_i
                try:
                    ceq = rxn_sys.equilibrium(c0, maintain=maintain,
                                              v_oa=v_oa if v_oa != 1.0 else None)
                except Exception:
                    res.extend([1e6] * len(eq_species))
                    continue
                for sp in eq_species:
                    obs = equilibria[i].get(sp, 0.0)
                    got = ceq.get(sp, 0.0)
                    if obs != 0.0:
                        res.append((got - obs) / obs)
                    else:
                        res.append(got)
        return np.array(res, dtype=float)

    _residuals._last_stoich_key = None
    return _residuals




def _validate_data(initial: dict, equilibrium: dict) -> Tuple[List[dict], List[dict]]:
    """
    Accept either single-point dicts or dict-of-lists and normalise to
    lists of dicts.
    """
    # detect dict-of-lists vs single dict
    def _is_dict_of_lists(d):
        return d and all(isinstance(v, (list, np.ndarray)) for v in d.values())

    if _is_dict_of_lists(initial):
        n = len(next(iter(initial.values())))
        initials = [{sp: float(v[i]) for sp, v in initial.items()}
                    for i in range(n)]
    else:
        initials = [initial]

    if _is_dict_of_lists(equilibrium):
        n = len(next(iter(equilibrium.values())))
        equilibria = [{sp: float(v[i]) for sp, v in equilibrium.items()}
                      for i in range(n)]
    else:
        equilibria = [equilibrium]

    if len(initials) != len(equilibria):
        raise InputError(
            f"initial and equilibrium must have the same number of data points, "
            f"got {len(initials)} and {len(equilibria)}.")

    return initials, equilibria


