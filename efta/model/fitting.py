# model — mathematical fitting
# ---------------------------------------------------------------------------

def _model(func: Callable,
          equilibrium: dict,
          initial: dict = None,
          maintain: list = None,
          *,
          x0: dict = None,
          tolerance: float = 1e-8,
          max_iter: int = 1000,
          v_oa: float = 1.0,
          solver_kw: dict = None) -> Model:
    """
    Mathematical fitting of a reaction model.

    Parameters are log-transformed internally to ensure positivity.
    Uses ``scipy.optimize.least_squares`` with the Trust Region Reflective
    method.

    Parameters
    ----------
    func : callable
        A function ``func(*params) -> reaction | reactions``.
        Parameter names are inferred from the function signature.
    equilibrium : dict
        Observed equilibrium concentrations.
        Either ``{species: value}`` (single point) or
        ``{species: [v1, v2, ...]}`` (multiple points).
    initial : dict, optional
        Initial concentrations. Same format as *equilibrium*.
        If ``None``, the observed equilibrium concentrations are used as
        the initial condition (self-consistency fitting).
    maintain : list of str, optional
        Species held fixed during equilibrium solving.
    x0 : dict, optional
        Initial guess ``{param_name: value}``.  Defaults to 1.0 for all.
    tolerance : float, optional
        Convergence tolerance for the optimiser.
    max_iter : int, optional
        Maximum number of function evaluations.
    solver_kw : dict, optional
        Extra keyword arguments forwarded to ``scipy.optimize.least_squares``.

    Returns
    -------
    Model

    Examples
    --------
    >>> def f(K):
    ...     return reaction('A = B', K)
    >>> result = model(f, equilibrium={'B': [0.04, 0.08]})
    >>> result = model(f, equilibrium={'B': [0.04, 0.08]},
    ...                initial={'A': [0.1, 0.2]})
    >>> result.popt   # (K_opt,)
    """
    maintain   = maintain or []
    solver_kw  = solver_kw or {}
    names      = _param_names(func)
    if initial is None:
        initial = equilibrium
    initials, equilibria = _validate_data(initial, equilibrium)
    eq_species = sorted({sp for eq in equilibria for sp in eq})

    # initial guess in direct space (not log)
    if x0 is None:
        x0_arr = np.ones(len(names))
    else:
        x0_arr = np.array([float(x0.get(n, 1.0)) for n in names])

        residuals_fn = _build_objective(func, initials, equilibria, maintain,
                                        eq_species, v_oa=v_oa)

    def _log_residuals(log_params):
        return residuals_fn(np.exp(log_params))

    def _log_obj_scalar(log_params):
        r = _log_residuals(log_params)
        return float(np.sum(r ** 2))

    x0_log = np.log(np.maximum(x0_arr, 1e-300))
    # Never start at exactly 0 — finite-diff step = eps*|x| = 0 when x=0.
    x0_log = np.where(x0_log == 0.0, 0.1, x0_log)

    # ── Primary: Nelder-Mead on scalar SSE ───────────────────────────────────
    # Does not rely on finite differences, works regardless of scale.
    # Initial simplex spans ±2 log-units around x0.
    from scipy.optimize import minimize
    nm_result = minimize(
        _log_obj_scalar, x0_log,
        method='Nelder-Mead',
        options={
            'maxiter':  max_iter,
            'xatol':    tolerance,
            'fatol':    tolerance,
            'initial_simplex': np.vstack([
                x0_log,
                *[x0_log + np.eye(len(names))[i] * 2.0
                  for i in range(len(names))],
            ]),
        }
    )
    best_log  = nm_result.x
    best_cost = nm_result.fun

    # ── Refine with least_squares from NM solution ───────────────────────────
    try:
        ls_result = least_squares(
            _log_residuals, best_log,
            method='lm',
            ftol=tolerance, xtol=tolerance,
            max_nfev=max_iter,
        )
        if ls_result.cost < best_cost:
            best_log  = ls_result.x
            best_cost = ls_result.cost
    except Exception:
        pass

    # make a result-like object for consistent attribute access
    class _R:
        pass
    ls_result = _R()
    ls_result.success = nm_result.success
    ls_result.message = nm_result.message
    ls_result.nfev    = int(nm_result.nfev)

    # ── Fallback: DE + polish only if least_squares genuinely failed ─────────
    # Note: rms > 0 is expected with real experimental data (measurement noise).
    # Only fall back to DE if the optimiser itself reports failure.
    if not ls_result.success:
        log_lo = np.full(len(names), -50.0)
        log_hi = np.full(len(names),  50.0)
        de_result = differential_evolution(
            _log_obj_scalar, list(zip(log_lo, log_hi)),
            seed=42, maxiter=max_iter // 5,
            tol=tolerance * 10, workers=1,
            x0=x0_log,
            **{k: v for k, v in solver_kw.items()
               if k in ('popsize', 'mutation', 'recombination', 'seed', 'workers')},
        )
        # polish DE result
        pol = least_squares(
            _log_residuals, de_result.x,
            method='trf',
            ftol=tolerance, xtol=tolerance, gtol=tolerance,
            max_nfev=max_iter,
            diff_step=0.01,
        )
        if pol.cost < best_cost:
            best_log  = pol.x
            ls_result = pol
            ls_result = type('R', (), {
                'success': pol.success or de_result.success,
                'message': pol.message,
                'nfev':    de_result.nfev + pol.nfev + ls_result.nfev,
            })()

    popt      = np.exp(best_log)
    final_res = residuals_fn(popt)

    return Model(
        popt        = popt,
        residuals   = final_res,
        success     = ls_result.success,
        message     = ls_result.message,
        n_iter      = ls_result.nfev,
        param_names = names,
    )


