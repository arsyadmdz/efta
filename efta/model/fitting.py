# model_chem — chemical fitting with constrained parameter spaces
# ---------------------------------------------------------------------------

def _neighbour_specs(gene_specs: dict, cont_params: dict) -> dict:
    """
    Build Phase-2a (exploitation) gene_specs from cont estimate.

    Discrete params → 2 nearest allowed values to cont estimate,
                      clipped to list boundaries.
    Continuous params → ±25% in log-space around cont estimate,
                        clipped to acceptable range.
                        Falls back to full range if clipped span is
                        < 10% of original log-span.
    """
    exploit_specs = {}
    for name, spec in gene_specs.items():
        v = cont_params.get(name, None)
        if v is None:
            exploit_specs[name] = spec
            continue

        if isinstance(spec, list):
            # snap to nearest 2 neighbours (or 1 if list is tiny)
            arr  = np.array(spec, dtype=float)
            diffs = np.abs(arr - float(v))
            order = np.argsort(diffs)
            n_nb  = min(2, len(arr))
            neighbours = sorted(arr[order[:n_nb]].tolist())
            exploit_specs[name] = neighbours

        else:
            lo, hi = spec
            log_lo = np.log(max(lo, 1e-300))
            log_hi = np.log(max(hi, lo * 1.001 + 1e-300))
            log_v  = np.log(max(float(v), 1e-300))

            # ±25% in log-space, clipped to [log_lo, log_hi]
            delta      = abs(log_v) * 0.25 if log_v != 0 else 1.0
            new_lo     = np.clip(log_v - delta, log_lo, log_hi)
            new_hi     = np.clip(log_v + delta, log_lo, log_hi)
            clipped_span = new_hi - new_lo

            # fallback to full range if clipped span is < 1 log-unit
            # (guarantees at least one order-of-magnitude of search space)
            if clipped_span < 1.0:
                new_lo, new_hi = log_lo, log_hi

            exploit_specs[name] = (float(np.exp(new_lo)), float(np.exp(new_hi)))

    return exploit_specs


# ---------------------------------------------------------------------------
# model_chem — chemical fitting with constrained parameter spaces
# ---------------------------------------------------------------------------

def _model_chem(func: Callable,
               equilibrium: dict,
               initial: dict = None,
               maintain: list = None,
               *,
               tolerance: float = 1e-8,
               generations: int = 100,
               sol_per_pop: int = 20,
               mating: int = 10,
               random_seed: int = 42,
               v_oa: float = 1.0,
               **param_constraints) -> Model:
    """
    Chemical fitting with per-parameter constraints.

    Three-phase strategy:

    **Phase 1 — Cont warm-up** (Nelder-Mead, ignores discrete constraints)
        Fast continuous estimate used to seed the GA.

    **Phase 2a — Exploitation** (25% generations, 50% population)
        Discrete params: 2 nearest neighbours to cont estimate.
        Continuous params: ±25% log-space window around cont estimate,
        clipped to acceptable range.

    **Phase 2b — Exploration** (75% generations, 100% population)
        Full acceptable range, full population, initialised from Phase-2a
        winner — so the search starts near the good region.
    """
    from .ga import GA

    maintain = maintain or []
    names    = _param_names(func)
    if initial is None:
        initial = equilibrium
    initials, equilibria = _validate_data(initial, equilibrium)
    eq_species = sorted({sp for eq in equilibria for sp in eq})

    # ── build gene_specs ─────────────────────────────────────────────────────
    gene_specs = {}
    for name in names:
        key  = f'acceptable_{name}'
        spec = param_constraints.get(key, (0, float('inf')))
        if isinstance(spec, (list, np.ndarray)):
            gene_specs[name] = list(spec)
        elif isinstance(spec, tuple) and len(spec) == 2:
            lo, hi = float(spec[0]), float(spec[1])
            if lo == 0 and hi == float('inf'):
                lo, hi = 1e-15, 1e15
            if lo >= hi:
                raise InputError(
                    f"acceptable_{name}: lower bound {lo} >= upper bound {hi}.")
            gene_specs[name] = (lo, hi)
        else:
            raise InputError(
                f"acceptable_{name} must be a (lo, hi) tuple or a list, "
                f"got {type(spec).__name__}.")

        residuals_fn = _build_objective(func, initials, equilibria, maintain,
                                        eq_species, v_oa=v_oa)
    total_evals  = [0]

    def fitness(params: dict):
        vals = [params[n] for n in names]
        res  = residuals_fn(vals)
        total_evals[0] += 1
        return -float(np.sum(res ** 2))

    # ── Phase 1: cont warm-up ─────────────────────────────────────────────────
    # For discrete params: seed at median of allowed list.
    # For continuous params: run Nelder-Mead for a fast estimate.
    # This avoids the unconstrained solver going to zero for discrete-like params.

    # seed discrete params at their median allowed value
    discrete_seed = {}
    for name, spec in gene_specs.items():
        if isinstance(spec, list):
            arr = sorted(spec)
            discrete_seed[name] = float(arr[len(arr) // 2])

    try:
        # Run Nelder-Mead with discrete params fixed at their median seed.
        # Wrap func to substitute discrete params before calling original func.
        if discrete_seed:
            orig_names = names

            def func_cont(*args):
                all_params = []
                cont_idx = 0
                cont_vals = list(args)
                for name in orig_names:
                    if name in discrete_seed:
                        all_params.append(discrete_seed[name])
                    else:
                        all_params.append(cont_vals[cont_idx])
                        cont_idx += 1
                return func(*all_params)

            import inspect as _inspect
            cont_only_names = [n for n in names if n not in discrete_seed]
            func_cont.__signature__ = _inspect.Signature([
                _inspect.Parameter(n, _inspect.Parameter.POSITIONAL_OR_KEYWORD)
                for n in cont_only_names
            ])
            cont_fit = _model(func_cont, equilibrium, initial=initial,
                              maintain=maintain,
                              tolerance=tolerance * 100, max_iter=500)
            cont_params = dict(discrete_seed)
            for n, v in zip(cont_only_names, cont_fit.popt):
                cont_params[n] = v
        else:
            cont_fit = _model(func, equilibrium, initial=initial,
                              maintain=maintain,
                              tolerance=tolerance * 100, max_iter=500)
            cont_params = {n: v for n, v in zip(names, cont_fit.popt)}
    except Exception:
        # fallback: median of discrete, geometric mean of continuous
        cont_params = {}
        for name, spec in gene_specs.items():
            if isinstance(spec, list):
                arr = sorted(spec)
                cont_params[name] = float(arr[len(arr) // 2])
            else:
                lo, hi = spec
                cont_params[name] = float(np.exp(
                    (np.log(max(lo,1e-300)) + np.log(max(hi,lo+1e-300))) / 2))

    # ── Phase 2a: exploitation ────────────────────────────────────────────────
    gen_exploit  = max(1, int(generations * 0.25))
    pop_exploit  = max(4, int(sol_per_pop * 0.5))
    exploit_specs = _neighbour_specs(gene_specs, cont_params)

    ga_exploit = GA(
        fitness_func  = fitness,
        gene_specs    = exploit_specs,
        generations   = gen_exploit,
        pop_size      = pop_exploit,
        n_parents     = max(2, pop_exploit // 2),
        early_stop    = -tolerance,
        seed          = random_seed,
    )
    sol_exploit = ga_exploit.run()

    # ── Phase 2b: exploration ─────────────────────────────────────────────────
    gen_explore = max(1, generations - gen_exploit)
    # seed first individual from Phase-2a winner
    ga_explore = GA(
        fitness_func  = fitness,
        gene_specs    = gene_specs,
        generations   = gen_explore,
        pop_size      = sol_per_pop,
        n_parents     = mating,
        early_stop    = -tolerance,
        seed          = random_seed + 1,
    )
    # inject Phase-2a best into initial population via custom init
    ga_explore._phase2a_seed = sol_exploit.params
    _orig_random = ga_explore.__class__.__dict__.get('run')

    # patch population initialisation to include Phase-2a seed
    from .ga import _random_individual, GA as _GA
    orig_run = ga_explore.run.__func__

    def run_with_seed(self):
        import numpy as _np
        from .ga import _random_individual, _decode_individual, _clip_individual
        rng     = _np.random.default_rng(self.seed)
        n_evals = 0

        # initialise population with Phase-2a seed in slot 0
        pop = _np.array([_random_individual(self._parsed, rng)
                         for _ in range(self.pop_size)])
        # encode seed
        seed_params = self._phase2a_seed
        for i2, (name2, kind2, spec2) in enumerate(self._parsed):
            v2 = seed_params.get(name2)
            if v2 is not None:
                if kind2 == 'continuous':
                    pop[0, i2] = _np.log(max(float(v2), 1e-300))
                else:
                    vals2 = spec2
                    idx2  = int(_np.argmin(
                        _np.abs(_np.array(vals2, dtype=float) - float(v2))))
                    pop[0, i2] = float(idx2)

        def _eval_pop(population):
            nonlocal n_evals
            scores = _np.empty(len(population))
            for j, ind in enumerate(population):
                params    = _decode_individual(ind, self._parsed)
                scores[j] = float(self.fitness_func(params))
                n_evals  += 1
            return scores

        fitness_arr = _eval_pop(pop)
        best_idx    = int(_np.argmax(fitness_arr))
        best_ind    = pop[best_idx].copy()
        best_fit    = float(fitness_arr[best_idx])
        best_gen    = 0

        for gen in range(self.generations):
            if self.early_stop is not None and best_fit >= self.early_stop:
                break
            parents   = self._select(pop, fitness_arr, rng)
            offspring = self._crossover(parents, rng)
            offspring = self._mutate(offspring, rng)
            off_fit   = _eval_pop(offspring)
            worst     = int(_np.argmin(off_fit))
            if best_fit > off_fit[worst]:
                offspring[worst] = best_ind.copy()
                off_fit[worst]   = best_fit
            pop         = offspring
            fitness_arr = off_fit
            gb          = int(_np.argmax(fitness_arr))
            if fitness_arr[gb] > best_fit:
                best_fit = float(fitness_arr[gb])
                best_ind = pop[gb].copy()
                best_gen = gen + 1

        from .ga import GASolution, _decode_individual
        return GASolution(
            params     = _decode_individual(best_ind, self._parsed),
            fitness    = best_fit,
            generation = best_gen,
            n_evals    = n_evals,
        )

    import types
    ga_explore.run = types.MethodType(run_with_seed, ga_explore)
    sol_explore = ga_explore.run()

    # ── pick best across all phases ────────────────────────────────────────────
    best_sol = sol_explore if sol_explore.fitness >= sol_exploit.fitness else sol_exploit

    popt      = [best_sol.params[n] for n in names]
    final_res = residuals_fn(popt)
    sse       = -best_sol.fitness

    return Model(
        popt        = popt,
        residuals   = final_res,
        success     = bool(sse < tolerance * 1e6),
        message     = (f"3-phase: cont+exploit({gen_exploit}gen)+explore({gen_explore}gen), "
                       f"SSE={sse:.3e}"),
        n_iter      = total_evals[0],
        param_names = names,
    )


