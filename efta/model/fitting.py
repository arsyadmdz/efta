def montecarlo(func, equilibrium: dict,
               initial: dict = None,
               *, noise=0.05, n: int = 100, seed: int = 42,
               training_points: float = 0.75,
               based_on: str = 'lev',
               v_oa: float = 1.0,
               **kwargs) -> 'Analyzed':
    """
    Monte Carlo uncertainty analysis by perturbing observed concentrations.

    Each iteration adds relative noise to equilibrium (and initial)
    concentrations, refits the model, and collects parameter values.
    The best result is selected by highest Levene p-value (most homoscedastic
    residuals).

    Method is auto-detected from stored constraints (same logic as
    :func:`model`).

    Parameters
    ----------
    func : freaction | freactions | callable
        Parameterised reaction system with optional stored constraints.
    equilibrium : dict
        Observed equilibrium concentrations.
    initial : dict, optional
        Initial concentrations.  If ``None``, uses *equilibrium* as c0.
    maintain : list, optional
        Species held fixed during equilibrium solving.
    noise : float or dict, optional
        Relative noise (1-sigma).
        ``0.05`` → 5% noise on all species (default).
        ``{'LaCl2[+]': 0.05, 'H[+]': 0.02}`` → per-species noise.
    n : int, optional
        Number of Monte Carlo iterations (default 100).
    seed : int, optional
        Random seed (default 42).
    **kwargs
        For ``method='cont'``: ``x0``, ``tolerance``, ``max_iter``.
        For ``method='ranged'``: ``acceptable_{name}``, ``generations``,
        ``sol_per_pop``, ``mating``, ``tolerance``.

    Returns
    -------
    Analyzed
        Same as :func:`analyze` but with ``param_std`` and ``n_matched``
        additionally populated.

    Examples
    --------
    >>> f = (freaction("$(x1)A = B, $(x2)").params(['a', 'K'])
    ...       .constrain(K=(1e-5, 1e5)))
    >>> result = montecarlo(f, eq, noise=0.05, n=100)
    >>> result.param_std    # {'K': 0.85}

    >>> f.constrain(a=[1,2,3])   # now ranged
    >>> result = montecarlo(f, eq, noise=0.05, n=100)
    >>> result.param_std    # {'K': 0.72}  — std from matched iterations only
    >>> result.n_matched    # e.g. 67
    """
    method = _infer_method(func)
    if method == 'cont':
        return _montecarlo(func, equilibrium, initial=initial,
                           noise=noise, n=n, seed=seed,
                           training_points=training_points,
                           based_on=based_on,
                           v_oa=v_oa, **kwargs)
    else:
        acceptable = _constraints_to_acceptable(func)
        acceptable.update({k: v for k, v in kwargs.items()
                           if k.startswith('acceptable_')})
        other_kw = {k: v for k, v in kwargs.items()
                    if not k.startswith('acceptable_')}
        return _montecarlo_chem(func, equilibrium, initial=initial,
                                noise=noise, n=n, seed=seed,
                                training_points=training_points,
                                based_on=based_on,
                                v_oa=v_oa, **acceptable, **other_kw)
