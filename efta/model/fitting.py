"""
efta.model.fitting
==================
Two fitting functions for reaction-based models:

:func:`model`
    Mathematical fitting — free parameters are log-transformed to ensure
    positivity.  Uses ``scipy.optimize.least_squares`` (residual-based).

:func:`model_chem`
    Chemical fitting — supports continuous ranges ``(a, b)`` and discrete
    sets ``[a, b, c]`` per parameter.  Uses differential evolution for global
    search followed by local polishing.

Both functions accept a user-defined callable *func* that takes the free
parameters and returns a :class:`~efta.reaction.reaction` or
:class:`~efta.reactions.reactions` object.  The optimiser calls
``.equilibrium(initial_point, maintain=maintain)`` internally.
"""

from __future__ import annotations

import inspect
import warnings
from typing import Callable, Dict, List, Optional, Sequence, Tuple, Union

import numpy as np
from scipy.optimize import least_squares, differential_evolution

from ..errors import InputError, ConvergenceError
