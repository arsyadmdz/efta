def _perturb(data_dict: dict, noise, rng: 'np.random.Generator') -> dict:
    """
    Return a copy of *data_dict* with each value perturbed by relative noise.

    *noise* is either a float (same for all species) or a dict
    ``{species: float}``.  Each value v is replaced by
    ``v * (1 + N(0, noise_for_species))``, clipped to a small positive floor.
    """
    out = {}
    for sp, v in data_dict.items():
        sigma = noise.get(sp, 0.05) if isinstance(noise, dict) else float(noise)
        if isinstance(v, (list, np.ndarray)):
            arr = np.asarray(v, dtype=float)
            perturbed = arr * (1.0 + rng.normal(0.0, sigma, size=arr.shape))
            out[sp] = np.maximum(perturbed, 1e-300).tolist()
        else:
            perturbed = float(v) * (1.0 + rng.normal(0.0, sigma))
            out[sp] = max(perturbed, 1e-300)
    return out


def _montecarlo(func, equilibrium, initial=None, maintain=None, *,
                noise=0.05, n=100, seed=42, training_points=0.75,
                based_on='lev', v_oa=1.0,
                x0=None, tolerance=1e-8, max_iter=1000,
                **kw):
    """
    Monte Carlo uncertainty for continuous fitting.
    Returns Analyzed with param_std and n_matched populated.
    """
    from .suggest import Analyzed, _eval_residuals, _stat_tests, _split, _subset
    import numpy as _np

    maintain = maintain or []
    rng      = _np.random.default_rng(seed)
    initials, equilibria = _validate_data(
        initial if initial is not None else equilibrium, equilibrium)
    eq_species = sorted({sp for eq in equilibria for sp in eq})
    names      = _param_names(func)
    n_points   = len(equilibria)

    all_popt   = []
    all_mse    = []
    all_mte    = []
    all_t      = []
    all_lev    = []
    all_train  = []
    all_test   = []

    for _ in range(n):
        eq_p = _perturb(_lists_to_dict(equilibria), noise, rng)
        c0_p = _perturb(_lists_to_dict(initials), noise, rng) if initial is not None else eq_p

        try:
            fit = _model(func, eq_p, initial=c0_p, maintain=maintain,
                         x0=x0, tolerance=tolerance, max_iter=max_iter, v_oa=v_oa, **kw)
        except Exception:
            continue

        init_l, eq_l = _validate_data(c0_p, eq_p)
        train_idx, test_idx = _split(n_points, training_points, rng)
        train_res = _eval_residuals(func, fit.popt, _subset(eq_l, train_idx),
                                    _subset(init_l, train_idx), eq_species, maintain)
        test_res  = _eval_residuals(func, fit.popt, _subset(eq_l, test_idx),
                                    _subset(init_l, test_idx),  eq_species, maintain)
        t_stat, t_pval, lev_stat, lev_p = _stat_tests(train_res, test_res)

        all_popt.append(fit.popt)
        all_mse.append(float(_np.mean(train_res**2)))
        all_mte.append(float(_np.mean(test_res**2)))
        all_t.append(t_pval)
        all_lev.append(lev_p)
        all_train.append(train_idx)
        all_test.append(test_idx)

    if not all_popt:
        raise RuntimeError("All Monte Carlo iterations failed.")

    _sort_key = {'lev': all_lev, 'mse': [-v for v in all_mse],
                 'mte': [-v for v in all_mte], 't': all_t}
    scores    = _sort_key.get(based_on, all_lev)
    best_idx  = int(_np.argmax(scores))
    best_popt = all_popt[best_idx]
    best_fit  = _model(func, equilibrium, initial=initial, maintain=maintain,
                       x0={nm: v for nm, v in zip(names, best_popt)},
                       tolerance=tolerance, max_iter=max_iter, v_oa=v_oa, **kw)

    dists     = {name: [p[i] for p in all_popt] for i, name in enumerate(names)}
    param_std = {name: float(_np.std(vals)) for name, vals in dists.items()}
    t_stat_b, t_pval_b, lev_stat_b, lev_p_b = _stat_tests(
        _np.array([all_mse[best_idx]]), _np.array([all_mte[best_idx]]))

    return Analyzed(
        fit          = best_fit,
        mse          = all_mse[best_idx],
        mte          = all_mte[best_idx],
        t_stat       = all_t[best_idx],
        t_pval       = all_t[best_idx],
        lev_stat     = all_lev[best_idx],
        lev_pval     = all_lev[best_idx],
        train_idx    = all_train[best_idx],
        test_idx     = all_test[best_idx],
        distributions= [dists[nm] for nm in names],
        mse_dist     = all_mse,
        mte_dist     = all_mte,
        t_dist       = all_t,
        lev_dist     = all_lev,
        param_std    = param_std,
        n_matched    = len(all_popt),
    )


def _lists_to_dict(list_of_dicts):
    if not list_of_dicts:
        return {}
    keys = list_of_dicts[0].keys()
    return {k: [d[k] for d in list_of_dicts] for k in keys}


def _montecarlo_chem(func, equilibrium, initial=None, maintain=None, *,
                     noise=0.05, n=100, seed=42, training_points=0.75,
                     based_on='lev', v_oa=1.0,
                     tolerance=1e-8, generations=100, mating=10,
                     **param_constraints):
    """
    Monte Carlo uncertainty for ranged/discrete fitting.
    Discrete params (list constraints) → filter to best stoich.
    Continuous params (tuple constraints) → param_std from filtered iterations.
    Returns Analyzed with param_std and n_matched populated.
    """
    from .suggest import Analyzed, _eval_residuals, _stat_tests, _split, _subset
    import numpy as _np

    maintain = maintain or []
    rng      = _np.random.default_rng(seed)
    initials, equilibria = _validate_data(
        initial if initial is not None else equilibrium, equilibrium)
    eq_species = sorted({sp for eq in equilibria for sp in eq})
    names      = _param_names(func)
    n_points   = len(equilibria)

    # identify discrete vs continuous params
    discrete_names   = set()
    continuous_names = set()
    for name in names:
        key = f'acceptable_{name}'
        if key in param_constraints:
            spec = param_constraints[key]
            if isinstance(spec, (list, _np.ndarray)):
                discrete_names.add(name)
            else:
                continuous_names.add(name)
        else:
            continuous_names.add(name)

    all_popt  = []
    all_mse   = []
    all_mte   = []
    all_t     = []
    all_lev   = []
    all_train = []
    all_test  = []

    for i_iter in range(n):
        eq_p = _perturb(_lists_to_dict(equilibria), noise, rng)
        c0_p = _perturb(_lists_to_dict(initials), noise, rng) if initial is not None else eq_p

        try:
            fit = _model_chem(func, eq_p, initial=c0_p, maintain=maintain,
                              tolerance=tolerance, generations=generations,
                              mating=mating, random_seed=seed + i_iter,
                              v_oa=v_oa, **param_constraints)
        except Exception:
            continue

        init_l, eq_l = _validate_data(c0_p, eq_p)
        train_idx, test_idx = _split(n_points, training_points, rng)
        train_res = _eval_residuals(func, fit.popt, _subset(eq_l, train_idx),
                                    _subset(init_l, train_idx), eq_species, maintain)
        test_res  = _eval_residuals(func, fit.popt, _subset(eq_l, test_idx),
                                    _subset(init_l, test_idx),  eq_species, maintain)
        t_stat, t_pval, lev_stat, lev_p = _stat_tests(train_res, test_res)

        all_popt.append(fit.popt)
        all_mse.append(float(_np.mean(train_res**2)))
        all_mte.append(float(_np.mean(test_res**2)))
        all_t.append(t_pval)
        all_lev.append(lev_p)
        all_train.append(train_idx)
        all_test.append(test_idx)

    if not all_popt:
        raise RuntimeError("All Monte Carlo iterations failed.")

    _sort_key = {'lev': all_lev, 'mse': [-v for v in all_mse],
                 'mte': [-v for v in all_mte], 't': all_t}
    scores    = _sort_key.get(based_on, all_lev)
    best_idx  = int(_np.argmax(scores))
    best_popt = all_popt[best_idx]

    # filter matched discrete stoich
    name_to_idx   = {name: i for i, name in enumerate(names)}
    best_discrete  = {name: best_popt[name_to_idx[name]] for name in discrete_names}
    matched = [
        popt for popt in all_popt
        if all(_np.isclose(popt[name_to_idx[name]], val, rtol=1e-6)
               for name, val in best_discrete.items())
    ] or all_popt

    dists     = {name: [p[name_to_idx[name]] for p in all_popt] for name in names}
    param_std = {name: float(_np.std([p[name_to_idx[name]] for p in matched]))
                 for name in continuous_names}

    best_fit  = _model_chem(func, equilibrium, initial=initial, maintain=maintain,
                            tolerance=tolerance, generations=max(10, generations // 10),
                            mating=mating, v_oa=v_oa, **param_constraints)

    return Analyzed(
        fit          = best_fit,
        mse          = all_mse[best_idx],
        mte          = all_mte[best_idx],
        t_stat       = all_t[best_idx],
        t_pval       = all_t[best_idx],
        lev_stat     = all_lev[best_idx],
        lev_pval     = all_lev[best_idx],
        train_idx    = all_train[best_idx],
        test_idx     = all_test[best_idx],
        distributions= [dists[nm] for nm in names],
        mse_dist     = all_mse,
        mte_dist     = all_mte,
        t_dist       = all_t,
        lev_dist     = all_lev,
        param_std    = param_std,
        n_matched    = len(matched),
    )


