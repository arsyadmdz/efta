def model(func, equilibrium: dict,
          initial: dict = None,
          v_oa: float = 1.0,
          **kwargs) -> 'Model':
    """
    Fit free parameters of a :class:`~efta.model.freaction` or
    :class:`~efta.model.freactions` to observed equilibrium data.

    Method is auto-detected from stored constraints:

    - All params ``(0, inf)`` → ``'cont'`` (Nelder-Mead, log-transformed).
    - Any param has a list or finite range → ``'ranged'`` (pygad GA).

    Use :meth:`freaction.constrain` to set constraints.

    Parameters
    ----------
    func : freaction | freactions
        Parameterised reaction system with optional stored constraints.
    equilibrium : dict
        Observed equilibrium concentrations.
    initial : dict, optional
        Initial concentrations. If ``None``, uses *equilibrium* as c0.
    **kwargs
        For ``'cont'``: ``x0``, ``tolerance``, ``max_iter``.
        For ``'ranged'``: ``generations``, ``sol_per_pop``, ``mating``.

    Returns
    -------
    Model

    Examples
    --------
    >>> f = (freaction("$(x1)A = B, $(x2)").params(['a', 'K'])
    ...       .constrain(a=[1,2,3], K=(1e-5,1e5)))
    >>> result = model(f, eq)
    >>> result.popt         # (a_opt, K_opt)
    """
    method = _infer_method(func)
    if method == 'cont':
        return _model(func, equilibrium, initial=initial,
                      v_oa=v_oa, **kwargs)
    else:
        acceptable = _constraints_to_acceptable(func)
        acceptable.update({k: v for k, v in kwargs.items()
                           if k.startswith('acceptable_')})
        other_kw = {k: v for k, v in kwargs.items()
                    if not k.startswith('acceptable_')}
        return _model_chem(func, equilibrium, initial=initial,
                           v_oa=v_oa, **acceptable, **other_kw)


def analyze(func, equilibrium: dict,
            initial: dict = None,
            v_oa: float = 1.0,
            **kwargs) -> list:
    """
    Bootstrap model selection via repeated train/test splits.

    Method is auto-detected from stored constraints (same logic as
    :func:`model`).

    Parameters
    ----------
    func : freaction | freactions
        Parameterised reaction system with optional stored constraints.
    equilibrium : dict
        Observed equilibrium concentrations (dict of lists).
    initial : dict, optional
    **kwargs
        ``n``, ``training_points``, ``best``, ``based_on``, ``x0``,
        ``seed``, ``tolerance``, ``max_iter``,
        ``generations``, ``mating`` (ranged only).

    Returns
    -------
    list of Analyzed

    Examples
    --------
    >>> f = (freaction("$(x1)A = B, $(x2)").params(['a', 'K'])
    ...       .constrain(a=[1,2,3], K=(1e-5,1e5)))
    >>> results = analyze(f, eq, n=100)
    >>> best = results[0]
    """
    from .suggest import _suggest, _suggest_chem
    method = _infer_method(func)
    if method == 'cont':
        return _suggest(func, equilibrium, initial=initial,
                        v_oa=v_oa, **kwargs)
    else:
        acceptable = _constraints_to_acceptable(func)
        acceptable.update({k: v for k, v in kwargs.items()
                           if k.startswith('acceptable_')})
        other_kw = {k: v for k, v in kwargs.items()
                    if not k.startswith('acceptable_')}
        return _suggest_chem(func, equilibrium, initial=initial,
                             v_oa=v_oa, **acceptable, **other_kw)


